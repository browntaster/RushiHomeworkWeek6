package week7MidHomework;
/* Question 2)
Print multiplication table of 24, 50 and 29 using loop.*/
public class Q2 {
    public static void main(String[] args) {
        System.out.println("Print multiplication table of 24: ");
        for (int i=0; i<=10; i++){
            System.out.println("24 * "+i+" = "+(24*i));
        }

        System.out.println("Print multiplication table of 50: ");
        for (int i=0; i<=10; i++){
            System.out.println("50 * "+i+" = "+(50*i));
        }

        System.out.println("Print multiplication table of 29: ");
        for (int i=0; i<=10; i++){
            System.out.println("29 * "+i+" = "+(29*i));
        }
    }
}
